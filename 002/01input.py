# input() 函数允许用户与程序进行互动，向程序传入数据以供运行
# 格式为：input("字符串，也就是给用户的提示")，举个栗子
input("宫廷玉液酒的下一句是什么：")
# 此时运行时 python 就会允许用户输入他的答案：“安全第一条”，敲下回车，python 才会运行之后的代码
# 不过现在你还完全用不了 input 获得的信息，此时只需用变量来获取即可啦
user_answer = input("宫廷玉液酒的下一句是什么：")
# 然后你就可以愉快地用你或得到的数据继续输出了！
print("宫廷玉液酒，"+user_answer+"！")

