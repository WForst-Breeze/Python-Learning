# 来试着获取用户的身高体重数据以计算其 BMI 吧！
height = input("您的身高是（单位：米）：")  # 用户输入身高
weight = input("您的体重是（单位：千克）：")  # 用户输入体重
bmi = float(weight) / (float(height)) ** 2    # 计算用户 bmi
print("您的 BMI 为："+str(bmi))  # 输出用户 bmi